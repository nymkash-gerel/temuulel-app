async function main() {
  console.log('Updating ai_context + product_faqs with size recommendations...\n')
  for (const u of updates) {
    const { error } = await sb.from('products')
      .update({ ai_context: u.ai_context, product_faqs: u.product_faqs })
      .eq('store_id', STORE_ID)
      .ilike('name', `%${u.name}%`)
    console.log(error ? `  🔴 ${u.name}: ${error.message}` : `  ✅ ${u.name}`)
  }

  const { data } = await sb.from('products')
    .select('name, ai_context')
    .eq('store_id', STORE_ID)
    .not('ai_context', 'is', null)
  console.log(`\n${data?.length || 0} products with ai_context + size info`)
}
main()
